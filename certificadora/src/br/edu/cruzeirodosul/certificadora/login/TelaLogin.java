    private void btnAcessarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAcessarMouseClicked
       if(StringFormatUtil.isNull(inputNomeUsuario.getText()) && StringFormatUtil.isNull(inputSenha.getText())){
           JOptionPane.showMessageDialog(null, "Por favor, informe o nome do usuário e Senha!");
       }else if(StringFormatUtil.isNull(inputNomeUsuario.getText())){
            JOptionPane.showMessageDialog(null,  "Por favor, informe o nome do usuário!");
        }else if(StringFormatUtil.isNull(inputSenha.getText())){
            JOptionPane.showMessageDialog(null,  "Por favor, informe senha!");
        }else{
            String nomeUsuario = inputNomeUsuario.getText();
            String senha = inputSenha.getText();
            System.out.println(inputNomeUsuario.getText()+" - "+inputSenha.getText());
            
            if(nomeUsuario.equals("jssantos") && senha.equals("jeff3112")){
                TelaPrincipal telaPrincipal = new TelaPrincipal();
                telaPrincipal.show();
                telaPrincipal.setLocationRelativeTo(null);
                this.hide();
            }else{
                JOptionPane.showMessageDialog(null,  "Usuário ou senha inválido!");
            }
        }
    }//GEN-LAST:event_btnAcessarMouseClicked

